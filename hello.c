#include<stdio.h>
int main()
{   // Digraph- two sequence character (\n , \r ,\t , \v)
    // <newline> character
    printf("1Hey rishabh!\n");
    // two blank line if i use \n\n
     printf("2Hey rishabh!\n\n");

    // <carriage return> character used for spacing or whitespace
    printf("3Hey rishabh!\r");
    printf("4Hey rishabh!\r\n");
    return 0;
}